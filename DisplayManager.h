// --- EYES DRAWING AND PHYSICALS MASKING ---

void drawEyelidMask(float x, float y, float w, float h, int mood, bool isLeft) {
  int ix = (int)x;
  int iy = (int)y;
  int iw = (int)w;
  int ih = (int)h;

  if (mood == MOOD_ANGRY) {
    if (isLeft)
      for (int i = 0; i < 16; i++) display.drawLine(ix, iy + i, ix + iw, iy - 6 + i, SSD1306_BLACK);
    else
      for (int i = 0; i < 16; i++) display.drawLine(ix, iy - 6 + i, ix + iw, iy + i, SSD1306_BLACK);
  }
  else if (mood == MOOD_SAD) {
    if (isLeft)
      for (int i = 0; i < 16; i++) display.drawLine(ix, iy - 6 + i, ix + iw, iy + i, SSD1306_BLACK);
    else
      for (int i = 0; i < 16; i++) display.drawLine(ix, iy + i, ix + iw, iy - 6 + i, SSD1306_BLACK);
  }
  else if (mood == MOOD_HAPPY || mood == MOOD_LOVE || mood == MOOD_EXCITED) {
    display.fillRect(ix, iy + ih - 10, iw, 12, SSD1306_BLACK);
    display.fillCircle(ix + iw / 2, iy + ih + 6, iw / 1.3, SSD1306_BLACK);
  }
  else if (mood == MOOD_SLEEPY) {
    display.fillRect(ix, iy, iw, ih / 2 + 2, SSD1306_BLACK);
  }
  else if (mood == MOOD_SUSPICIOUS) {
    if (isLeft) display.fillRect(ix, iy, iw, ih / 2 - 2, SSD1306_BLACK);
    else display.fillRect(ix, iy + ih - 8, iw, 8, SSD1306_BLACK);
  }
}

void drawSunglasses(int lx, int ly, int lw, int lh, int rx, int ry, int rw, int rh) {
  display.fillRect(lx - 2, ly + 2, lw + 4, lh - 6, SSD1306_BLACK);
  display.drawRoundRect(lx - 2, ly + 2, lw + 4, lh - 6, 3, SSD1306_WHITE);
  display.fillRect(rx - 2, ry + 2, rw + 4, rh - 6, SSD1306_BLACK);
  display.drawRoundRect(rx - 2, ry + 2, rw + 4, rh - 6, 3, SSD1306_WHITE);
  display.drawLine(lx + lw - 2, ly + lh/2 - 2, rx + 2, ry + rh/2 - 2, SSD1306_WHITE);
  display.drawLine(lx + lw - 2, ly + lh/2 - 3, rx + 2, ry + rh/2 - 3, SSD1306_WHITE);
  display.drawLine(0, ly + lh/2 - 2, lx - 2, ly + lh/2 - 2, SSD1306_WHITE);
  display.drawLine(rx + rw + 2, ry + rh/2 - 2, 127, ry + rh/2 - 2, SSD1306_WHITE);
}

void drawUltraProEye(Eye& e, bool isLeft, int mood) {
  int ix = (int)e.x;
  int iy = (int)e.y;
  int iw = (int)e.w;
  int ih = (int)e.h;

  int r = 6;
  if (iw < 20) r = 3;

  if (currentEyeStyle == STYLE_CLASSIC) {
    display.fillRoundRect(ix, iy, iw, ih, r, SSD1306_WHITE);
  } else if (currentEyeStyle == STYLE_BOLD) {
    display.fillRoundRect(ix - 1, iy - 1, iw + 2, ih + 2, r + 1, SSD1306_WHITE);
  } else if (currentEyeStyle == STYLE_ROUND) {
    display.fillCircle(ix + iw / 2, iy + ih / 2, min(iw, ih) / 2 + 1, SSD1306_WHITE);
  } else if (currentEyeStyle == STYLE_SQUINTY) {
    display.fillRoundRect(ix, iy + ih / 4, iw, ih / 2, r / 2, SSD1306_WHITE);
  }

  int cx = ix + iw / 2;
  int cy = iy + ih / 2;
  int pw = iw / 2.2;
  int ph = ih / 2.2;

  int px = cx + (int)e.pupilX - (pw / 2);
  int py = cy + (int)e.pupilY - (ph / 2);

  int clampYMin = iy;
  int clampYMax = iy + ih - ph;
  if (currentEyeStyle == STYLE_SQUINTY) {
    ph = ih / 4;
    py = cy + (int)e.pupilY / 2 - (ph / 2);
    clampYMin = iy + ih / 4;
    clampYMax = iy + 3 * ih / 4 - ph;
  }

  if (px < ix) px = ix;
  if (px + pw > ix + iw) px = ix + iw - pw;
  if (py < clampYMin) py = clampYMin;
  if (py + ph > clampYMax) py = clampYMax;

  display.fillRoundRect(px, py, pw, ph, r / 2, SSD1306_BLACK);

  if (iw > 15 && ih > 15 && currentEyeStyle != STYLE_SQUINTY) {
    display.fillCircle(px + pw - 3, py + 3, 1, SSD1306_WHITE);
  }

  drawEyelidMask(e.x, e.y, e.w, e.h, mood, isLeft);
}

void drawMouth(int cx, int cy, int mood) {
  switch (mood) {
    case MOOD_HAPPY:
    case MOOD_LOVE:
    case MOOD_EXCITED:
      display.fillCircle(cx, cy, 6, SSD1306_WHITE);
      display.fillRect(cx - 7, cy - 7, 14, 8, SSD1306_BLACK);
      break;
    case MOOD_SAD:
    case MOOD_ANGRY:
      display.fillCircle(cx, cy, 6, SSD1306_WHITE);
      display.fillRect(cx - 7, cy - 1, 14, 8, SSD1306_BLACK);
      break;
    case MOOD_SURPRISED:
      display.drawCircle(cx, cy, 4, SSD1306_WHITE);
      display.drawCircle(cx, cy, 5, SSD1306_WHITE);
      break;
    case MOOD_SLEEPY:
      display.drawCircle(cx, cy, 2, SSD1306_WHITE);
      break;
    case MOOD_SUSPICIOUS:
      for (int t = 0; t < 2; t++) {
        display.drawLine(cx - 10, cy + 2 + t, cx + 10, cy - 2 + t, SSD1306_WHITE);
      }
      break;
    case MOOD_COOL:
      for (int t = 0; t < 2; t++) {
        display.drawLine(cx - 6, cy + t, cx + 6, cy - 2 + t, SSD1306_WHITE);
      }
      break;
    case MOOD_NORMAL:
    default:
      for (int t = 0; t < 2; t++) {
        display.drawLine(cx - 8, cy + t, cx + 8, cy + t, SSD1306_WHITE);
      }
      break;
  }
}

